package Lab2;

public class Lab2 {
	public static void main(String[] args) {
	    double radius = 5.5; // Declare radius
	    double area; // Declare area
	    double perimeter; // Declare perimeter
	    area = radius * radius * 3.14159;
	    perimeter = 2 * radius * 3.14159;
	    // Display results
	    System.out.println("The area for the circle" + 
			  " of radius " + radius + " is " + area);
	    System.out.println("The perimeter for the circle" + 
				  " of radius " + radius + " is " + perimeter);
	    
	  }
}



	
